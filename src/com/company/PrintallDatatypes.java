package com.company;

public class PrintallDatatypes {
    public static void main(String[] args) {
        byte myByteNum = 78;
        short myShort =897;
        int myNum = 57884;
        long my_long = 1618916826789098l;
        float myFloatNum = 5.99f;
        double myDoubleNum = 8098.5956654;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";
        System.out.println("Byte: "+myByteNum);
        System.out.println("Short: "+myShort);
        System.out.println("Integer: "+myNum);
        System.out.println("Long: "+my_long);
        System.out.println("Float: "+myFloatNum);
        System.out.println("Double: "+myDoubleNum);
        System.out.println("Character: "+myLetter);
        System.out.println("Boolean: "+myBool);
        System.out.println("String: "+myText);
    }
}
